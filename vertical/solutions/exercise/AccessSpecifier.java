package com.vertical.solutions.exercise;

public class AccessSpecifier {
	public void test1() {
		System.out.println("test 1");
	}
	private void test2() {
		System.out.println("test 2");
	}
	protected void test3() {
		System.out.println("test 3");
	}
	void test4() {
		System.out.println("Test 4");
	}
}
